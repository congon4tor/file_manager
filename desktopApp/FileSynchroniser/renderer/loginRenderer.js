const { ipcRenderer } = require('electron');
var form = document.getElementById('loginForm');
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
form.addEventListener('submit', (evt) => {
    evt.preventDefault();
    const username = evt.target[0].value;
    const password = evt.target[1].value;
    if (form.target) {
        ipcRenderer.send(form.target, username, password)
    }else{
        ipcRenderer.send('login', username, password)
    }
})
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
document.getElementById('signup').onclick = function () {
    form.target = 'signup';
    form.submit();
}
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
ipcRenderer.on('signup', (event, result) => {
    alert('Successfully signed up!')
})
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
ipcRenderer.on('login:Error', (event, result) => {
    alert(result)
})
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
ipcRenderer.on('signup:Error', (event, result) => {
    alert(result)
})
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
function alert(message) {
    if (message != "")
        document.getElementById("alert-message").innerHTML = message;
    $(".alert-bottom").show();
    setTimeout(function () {
        $(".alert-bottom").hide();
    }, 6000);
}
